#include<string.h>
#include<stdio.h>
#include<stdlib.h>
#include"string_base.h"

/************************************************************************
 *
 * 除去字符串开头的空白符
 *
************************************************************************/

void ltrim(char *s){
    char *p;
    p = s;
    while(*p == ' ' || *p == '\t'){*(p++);}
    strcpy(s,p);
}

/************************************************************************
 *
 * 除去字符串末尾的空白符
 *
************************************************************************/

static void rtrim(char *s){
    int i;
    i = strlen(s)-1;
    while((s[i] == ' ' || s[i] == '\t') && i >= 0){i--;};
    s[i+1] = '\0';
}


/************************************************************************ 
 *
 * 除去字符串开头和末尾的空白符
 *
************************************************************************/
void trim(char *s){
    ltrim(s);
    rtrim(s);
}

/************************************************************************ 
 *
 * 读取输入字符
 *
************************************************************************/
void getString(char *arr) {
    char a = getchar();
    int i  = 0;
    while (a !='\n') {
        arr[i++] = a;
        a = getchar();
    }
    arr[i]='\0';
    trim(arr);
}
